//contoh penggunaan if-else pada javascript

nilai = 6 > 5;
if (nilai) {
    console.log("benar");
  }else{
    console.log("salah");
  }

// contoh penggunaan switch-case

nilai = 1 > 5;
switch(nilai){
case true:
 console.log("benar");
 break;
case false:
 console.log("salah");
 break;
}

